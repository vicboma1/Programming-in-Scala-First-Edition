object ArithmeticOperations {
  def main (args: Array[String]) {
    println(1.2 + 2.3)
    println(3 - 1)
    println('b' - 'a')
    println(2L * 3L)
    println(11 % 4)
    println(11 / 4)
    println(11.0f / 4.0f)
    println(11.0 % 4.0)
  }
}
